// Switch

let fruit = 'Manzana';
let messeger
switch(fruit){
    case 'Naranja':
        messeger = 'Oh! Te gusta mucho la Naranja';
        break;
    case 'Manzana':
        messeger = 'Oh! Te gusta mucho la Manzana';
        break;
    case 'Papaya':
        messeger = 'Oh!, Te gusta mucho la Papaya';
        break;
    case 'Piña':
        messeger = 'Oh! Te gusta mucho la Piña'
        break;
    default:
        'No te gusta ninguna fruta';
};

console.log(messeger)